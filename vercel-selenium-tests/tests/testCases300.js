function getCategoryName(tcId) {
  const num = parseInt(tcId.replace("TC-", ""), 10);
  if (num <= 25) return "1. Public Landing Page & Navigation";
  if (num <= 50) return "2. Operator Sign-In & Authentication";
  if (num <= 75) return "3. Registration & Account Setup";
  if (num <= 100) return "4. Main Dashboard & Overview Widgets";
  if (num <= 125) return "5. Real-time Risk Signals & Alerts";
  if (num <= 150) return "6. Global Supply Chain Map & 3D Globe";
  if (num <= 175) return "7. Suppliers & Vendor Management";
  if (num <= 200) return "8. Inventory & Warehouse Tracking";
  if (num <= 225) return "9. Customers & Logistics Network";
  if (num <= 250) return "10. Trade Requests & Approvals";
  if (num <= 275) return "11. Manufacturing Facilities & Factories";
  return "12. AI Supply Chain Assistant & Analytics";
}

